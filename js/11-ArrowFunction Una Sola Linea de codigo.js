// Arrow funcion un solo parametro
const funcion2 = lenguaje => `soy programador de ${lenguaje}`;

console.log(funcion2('JavaScript'));


// Arrow funcion un mas parametros parametro
const funcion3 = (lenguaje, lenguaje2) => `soy programador de ${lenguaje} y ${lenguaje2}`;

console.log(funcion3('JavaScript', 'Python'));